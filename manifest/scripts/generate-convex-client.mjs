#!/usr/bin/env node
/**
 * generate-convex-client — typed frontend client for compile-to-Convex path.
 * Reads → Convex queries (tenant-scoped). Writes → executeCommand → generated mutations.
 *
 * Run: pnpm manifest:client
 */
import { execSync } from "node:child_process";
import {
  mkdtempSync,
  readdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { paramTsType, toTsTypes } from "./scalar-type-map.mjs";

const conv = JSON.parse(
  readFileSync("manifest/capsule-conventions.json", "utf8")
);
const ir = JSON.parse(readFileSync("manifest/ir/kitchen.ir.json", "utf8"));
const camel = (s) => s.charAt(0).toLowerCase() + s.slice(1);
const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1);
const plural = (s) =>
  /(s|x|z|ch|sh)$/.test(s)
    ? `${s}es`
    : /y$/.test(s)
      ? `${s.slice(0, -1)}ies`
      : `${s}s`;

const tdir = mkdtempSync(join(tmpdir(), "mf-types-"));
execSync(
  `pnpm exec manifest generate manifest/ir/kitchen.ir.json --surface types --output "${tdir}"`,
  { stdio: "pipe" }
);
function find(dir, name) {
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) {
      const r = find(p, name);
      if (r) return r;
    } else if (e === name) return p;
  }
  return null;
}
const typesFile = find(tdir, "manifest-generated.ts");
const tsTypes = toTsTypes(readFileSync(typesFile, "utf8"));
const enumDefs = (ir.enums ?? [])
  .map(
    (e) =>
      `export type ${e.name} = ${
        (e.values ?? []).map((v) => JSON.stringify(v.name)).join(" | ") || "string"
      };`
  )
  .join("\n");
writeFileSync(
  conv.client.typesOutput,
  "// GENERATED by manifest:client (manifest/scripts/generate-convex-client.mjs) — DO NOT EDIT.\n" +
    (enumDefs ? enumDefs + "\n\n" : "") +
    tsTypes
);
const typeSet = new Set(
  [...tsTypes.matchAll(/export interface (\w+)/g)].map((m) => m[1])
);

const entityNames = (ir.entities ?? []).map((e) => e.name).sort();
const usedTypes = new Set();
const out = [];
out.push("// GENERATED by manifest:client — DO NOT EDIT. Compile-to-Convex client.");
out.push("// Reads: Convex queries. Writes: generated mutations via executeCommand.");
out.push('import { fetchConvexListBrowser as fetchConvexList, fetchConvexRecordBrowser as fetchConvexRecord } from "@/app/lib/convex/read-bridge-browser";');
out.push('import { executeCommand } from "@/app/lib/manifest-client";');
out.push("__TYPE_IMPORT__");
out.push("");
out.push("export interface PaginatedResponse<T> {");
out.push("  data: T[];");
out.push("  pagination: { page: number; limit: number; total: number; totalPages: number };");
out.push("}");
out.push("");

for (const entity of entityNames) {
  const T = typeSet.has(entity) ? entity : "Record<string, unknown>";
  if (typeSet.has(entity)) usedTypes.add(entity);
  out.push(
    `export async function list${plural(entity)}(_query?: Record<string, string | number>): Promise<PaginatedResponse<${T}>> {`
  );
  out.push(`  const data = (await fetchConvexList(${JSON.stringify(entity)})) as ${T}[];`);
  out.push("  return { data, pagination: { page: 1, limit: data.length, total: data.length, totalPages: 1 } };");
  out.push("}");
  out.push(`export async function get${entity}(id: string): Promise<${T} | undefined> {`);
  out.push(`  return (await fetchConvexRecord(${JSON.stringify(entity)}, id)) as ${T} | undefined;`);
  out.push("}");
}

const seen = new Set();
const interfaces = [];
for (const c of ir.commands) {
  const key = `${c.entity}.${c.name}`;
  if (seen.has(key)) continue;
  seen.add(key);
  const T = typeSet.has(c.entity) ? c.entity : "unknown";
  if (typeSet.has(c.entity)) usedTypes.add(c.entity);
  const fn = `${camel(c.entity)}${cap(c.name)}`;
  let inputType = "Record<string, unknown>";
  if (c.parameters?.length > 0) {
    const iName = `${c.entity}${cap(c.name)}Input`;
    const fields = c.parameters.map((p) => `  ${p.name}?: ${paramTsType(p)};`);
    const hasId = c.parameters.some((p) => p.name === "id");
    if (!hasId && c.name !== "create") fields.unshift("  id?: string;");
    interfaces.push(`export interface ${iName} {\n${fields.join("\n")}\n}`);
    inputType = iName;
  } else if (c.name !== "create") {
    const iName = `${c.entity}${cap(c.name)}Input`;
    interfaces.push(`export interface ${iName} {\n  id?: string;\n}`);
    inputType = iName;
  }
  out.push(`export async function ${fn}(input: ${inputType} = {}): Promise<${T} | undefined> {`);
  out.push(
    `  const r = await executeCommand<${T}>(${JSON.stringify(c.entity)}, ${JSON.stringify(c.name)}, input as Record<string, unknown>);`
  );
  out.push("  return r.result;");
  out.push("}");
}

const importLine = usedTypes.size
  ? `import type { ${[...usedTypes].sort().join(", ")} } from "./manifest-types.generated";`
  : "";
const ifaceBlock =
  interfaces.length > 0
    ? "// Command input types (generated from IR parameters)\n" + interfaces.join("\n\n") + "\n\n"
    : "";
writeFileSync(
  conv.client.clientOutput,
  `${out.join("\n").replace("__TYPE_IMPORT__", importLine)}\n${ifaceBlock}`
);

console.log("WROTE", conv.client.typesOutput, `(${typeSet.size} types)`);
console.log("WROTE", conv.client.clientOutput, `(${entityNames.length} entities, ${seen.size} commands)`);
