#!/usr/bin/env node
/**
 * enforce-convex-architecture.mjs — structural guardrails for the
 * compile-to-Convex clone. Default mode: baseline-gated (block NEW violations).
 * --strict: fail on any violation outside legacy allowlist.
 * --save-baseline: refresh manifest/governance/baselines/convex-architecture.json
 */
import { readFileSync, readdirSync, statSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "../..");
const BASELINE_PATH = resolve(
  ROOT,
  "manifest/governance/baselines/convex-architecture.json"
);

/** Paths still being migrated — violations here warn only in strict mode. */
const LEGACY_ALLOWLIST_PREFIXES = [
  "packages/database/",
  "manifest/runtime/",
  "manifest/generated/runtime/",
  "apps/api/app/api/",
  "packages/supplier-connectors/src/sync-service.ts",
];

const FORBIDDEN = [
  {
    id: "prisma-client-import",
    re: /from\s+["']@prisma\/client["']/,
    message: "Do not import @prisma/client — use Convex projection.",
  },
  {
    id: "repo-database-import",
    re: /from\s+["']@repo\/database["']/,
    message: "Do not import @repo/database — use Convex client/queries.",
  },
  {
    id: "store-provider",
    re: /\bstoreProvider\s*[:=]/,
    message: "storeProvider is runtime-interpreter path — not used in compile-to-Convex.",
  },
  {
    id: "prisma-override",
    re: /\bprismaOverride\b/,
    message: "prismaOverride is legacy Prisma transaction bridging.",
  },
  {
    id: "create-manifest-runtime",
    re: /\bcreateManifestRuntime\s*\(/,
    message: "createManifestRuntime is runtime-interpreter path — call generated Convex mutations.",
  },
  {
    id: "hand-convex-mutation",
    re: /\bmutation\s*\(\s*\{[\s\S]*handler:\s*async\s*\(ctx/,
    message: "Hand-written Convex mutations forbidden outside generated convex/ files.",
    onlyUnder: ["convex/"],
    requireGeneratedHeader: true,
  },
];

const SCAN_DIRS = ["apps/app", "apps/api", "packages", "manifest/runtime/src", "convex"];
const SKIP = new Set(["node_modules", ".next", ".next-dev", "dist", "_generated", ".turbo"]);

const args = new Set(process.argv.slice(2));
const strict = args.has("--strict");
const saveBaseline = args.has("--save-baseline");

function walk(dir, out = []) {
  let entries;
  try {
    entries = readdirSync(dir);
  } catch {
    return out;
  }
  for (const name of entries) {
    if (SKIP.has(name)) continue;
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, out);
    else if (/\.(ts|tsx|js|mjs)$/.test(name)) out.push(full);
  }
  return out;
}

function isLegacy(rel) {
  return LEGACY_ALLOWLIST_PREFIXES.some((p) => rel.startsWith(p));
}

function scanViolations() {
  const findings = [];

  for (const dir of SCAN_DIRS) {
    const abs = join(ROOT, dir);
    for (const file of walk(abs)) {
      const rel = relative(ROOT, file).replace(/\\/g, "/");
      const text = readFileSync(file, "utf8");

      for (const rule of FORBIDDEN) {
        if (rule.onlyUnder && !rule.onlyUnder.some((p) => rel.startsWith(p))) continue;
        if (!rule.onlyUnder && rel.startsWith("convex/")) continue;
        if (rule.requireGeneratedHeader && text.includes("GENERATED by the Manifest")) {
          continue;
        }
        if (rel.startsWith("convex/lib/") || rel === "convex/auth.config.ts" || rel === "convex/identity.ts") {
          continue;
        }
        if (!rule.re.test(text)) continue;

        findings.push({
          file: rel,
          ruleId: rule.id,
          message: rule.message,
          legacy: isLegacy(rel),
        });
      }
    }
  }

  return findings;
}

function loadBaseline() {
  try {
    const raw = JSON.parse(readFileSync(BASELINE_PATH, "utf8"));
    return new Set(
      (raw.entries ?? []).map((e) => `${e.file}\0${e.ruleId}`)
    );
  } catch {
    return new Set();
  }
}

function saveBaselineFile(findings) {
  mkdirSync(dirname(BASELINE_PATH), { recursive: true });
  const payload = {
    version: 1,
    note:
      "Baseline of compile-to-Convex architecture violations. CI blocks NEW (file, ruleId) pairs. Regenerate with --save-baseline only after intentional migration.",
    entries: findings
      .filter((f) => !f.legacy)
      .map(({ file, ruleId, message }) => ({ file, ruleId, message }))
      .sort((a, b) => a.file.localeCompare(b.file) || a.ruleId.localeCompare(b.ruleId)),
  };
  writeFileSync(BASELINE_PATH, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
  console.log(`Saved ${payload.entries.length} baseline entries to ${relative(ROOT, BASELINE_PATH)}`);
}

function main() {
  const findings = scanViolations();
  const actionable = findings.filter((f) => !f.legacy);

  if (saveBaseline) {
    saveBaselineFile(findings);
    process.exit(0);
  }

  for (const f of findings.filter((x) => x.legacy)) {
    console.warn(`[legacy] ${f.file}: ${f.message} (${f.ruleId})`);
  }

  if (strict) {
    if (actionable.length) {
      console.error(`\n${actionable.length} architecture violation(s):\n`);
      for (const f of actionable) {
        console.error(`  ${f.file}: ${f.message} (${f.ruleId})`);
      }
      process.exit(1);
    }
    console.log("Convex architecture enforcement passed (strict).");
    return;
  }

  const baseline = loadBaseline();
  const newViolations = actionable.filter(
    (f) => !baseline.has(`${f.file}\0${f.ruleId}`)
  );

  if (newViolations.length) {
    console.error(`\n${newViolations.length} NEW architecture violation(s) (not in baseline):\n`);
    for (const f of newViolations) {
      console.error(`  ${f.file}: ${f.message} (${f.ruleId})`);
    }
    console.error(
      `\nMigrate off legacy patterns or run: node manifest/scripts/enforce-convex-architecture.mjs --save-baseline`
    );
    process.exit(1);
  }

  console.log(
    `Convex architecture enforcement passed (${actionable.length} baselined, 0 new).`
  );
}

main();
