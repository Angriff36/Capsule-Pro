#!/usr/bin/env node
// Emit feature-ownership.json: every entity -> its canonical product feature
// (derived from the manifest/source/<area>/<feature>/ path after the taxonomy
// reorg) + API route segment + command list. This is the analyzer's PRIMARY
// ownership signal: hierarchy = ownership, so the path answers "what feature
// owns this" without heuristics. Regenerate after moving/adding source files.
//
//   node manifest/scripts/emit-feature-ownership.mjs [--check]
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  "../.."
);
const SRC = path.join(ROOT, "manifest/source");
const OUT = path.join(ROOT, "manifest/feature-ownership.json");
const CHECK = process.argv.includes("--check");
const norm = (p) => p.replace(/\\/g, "/");

function* walk(dir) {
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) {
      yield* walk(full);
    } else if (e.name.endsWith(".manifest")) {
      yield full;
    }
  }
}

// feature = the source dir under manifest/source, minus the filename.
// e.g. operations/kitchen/dish-rules.manifest -> "operations/kitchen"
//      accounting/payroll-rules.manifest      -> "accounting"
function featureOf(absFile) {
  const rel = norm(path.relative(SRC, absFile));
  const dir = path.posix.dirname(rel);
  return dir === "." ? "(root)" : dir;
}

const SEGMENT_RE = /^\s{6,}([A-Z][A-Za-z0-9]+):\s*"([^"/]+\/[^"]+)"\s*$/;
const BLOCK_EXIT_RE = /^\s{0,4}\w/;

// routeSegments: entity -> API path, read straight from the config YAML block
// (simple line scan — avoids a YAML dep; the block is `Entity: "seg/ment"`).
function readRouteSegments() {
  const cfg = fs.readFileSync(path.join(ROOT, "manifest.config.yaml"), "utf8");
  const seg = {};
  const start = cfg.indexOf("routeSegments:");
  const slice = start >= 0 ? cfg.slice(start) : cfg;
  for (const line of slice.split("\n")) {
    const mm = SEGMENT_RE.exec(line);
    if (mm) {
      seg[mm[1]] = mm[2];
    } else if (BLOCK_EXIT_RE.test(line) && !line.includes("routeSegments")) {
      break; // left the routeSegments block
    }
  }
  return seg;
}

const ENTITY_RE = /^\s*(?:external\s+)?entity\s+([A-Z][A-Za-z0-9]*)/gm;
const COMMAND_RE = /^\s*(?:async\s+)?command\s+([a-z][A-Za-z0-9]*)\s*\(/gm;

const segments = readRouteSegments();
const entities = {};

for (const abs of walk(SRC)) {
  if (path.basename(abs) === "_base.manifest") {
    continue;
  }
  const src = fs.readFileSync(abs, "utf8");
  const feature = featureOf(abs);
  const sourceFile = norm(path.relative(ROOT, abs));
  // split file into per-entity chunks so commands attach to the right entity
  const names = [...src.matchAll(ENTITY_RE)].map((m) => ({
    name: m[1],
    idx: m.index,
  }));
  for (let i = 0; i < names.length; i++) {
    const { name, idx } = names[i];
    const end = i + 1 < names.length ? names[i + 1].idx : src.length;
    const body = src.slice(idx, end);
    const commands = [...body.matchAll(COMMAND_RE)].map((m) => m[1]);
    entities[name] = {
      feature,
      sourceFile,
      apiSegment: segments[name] ?? null,
      commands: commands.sort(),
    };
  }
}

const ordered = Object.fromEntries(
  Object.keys(entities)
    .sort()
    .map((k) => [k, entities[k]])
);
const payload = {
  $comment:
    "GENERATED by manifest/scripts/emit-feature-ownership.mjs. Ownership = source path. Do not hand-edit.",
  entityCount: Object.keys(ordered).length,
  features: [...new Set(Object.values(ordered).map((e) => e.feature))].sort(),
  entities: ordered,
};
const json = `${JSON.stringify(payload, null, 2)}\n`;

if (CHECK) {
  const cur = fs.existsSync(OUT) ? fs.readFileSync(OUT, "utf8") : "";
  if (cur !== json) {
    console.error(
      "feature-ownership.json is stale. Run: node manifest/scripts/emit-feature-ownership.mjs"
    );
    process.exit(1);
  }
  console.log(
    `feature-ownership.json in sync (${payload.entityCount} entities).`
  );
} else {
  fs.writeFileSync(OUT, json);
  console.log(
    `wrote ${norm(path.relative(ROOT, OUT))}: ${payload.entityCount} entities across ${payload.features.length} features.`
  );
}
