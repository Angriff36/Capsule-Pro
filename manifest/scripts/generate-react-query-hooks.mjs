#!/usr/bin/env node
/**
 * generate-react-query-hooks — Tier 6: generates TanStack Query hooks wrapping
 * the existing generated client (manifest-client.generated.ts).
 *
 * Strategy: wraps listX/getX/executeCommand functions as useQuery/useMutation
 * queryFn sources. This reuses the correct domain-mapped paths from the
 * generated client instead of deriving them from entity names.
 *
 * Usage:
 *   node manifest/scripts/generate-react-query-hooks.mjs [--outdir <path>]
 *     --outdir  output directory (default: manifest/generated/hooks)
 *
 * Output: manifest-hooks.generated.ts with queryKeys + hooks for all entities.
 */
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const IR_PATH = resolve("manifest/ir/kitchen.ir.json");

// Parse args
const args = process.argv.slice(2);
let outDir = resolve("apps/app/app/lib");
for (let i = 0; i < args.length; i++) {
  if (args[i] === "--outdir" && args[i + 1]) {
    outDir = resolve(args[i + 1]);
    i++;
  }
}

const CLIENT_PATH = resolve("apps/app/app/lib/manifest-client.generated.ts");

if (!existsSync(IR_PATH)) {
  console.error(`IR not found at ${IR_PATH}. Run pnpm manifest:compile first.`);
  process.exit(1);
}

if (!existsSync(CLIENT_PATH)) {
  console.error(
    `Generated client not found at ${CLIENT_PATH}. Run manifest:generate-client first.`
  );
  process.exit(1);
}

// Load IR to get entity names and command info
const ir = JSON.parse(readFileSync(IR_PATH, "utf-8"));
const entities = ir.entities ?? [];
console.log(`Loaded IR: ${entities.length} entities`);

// Extract exported function names from the generated client
const clientSource = readFileSync(CLIENT_PATH, "utf-8");
const exportedFnRegex = /export async function (\w+)/g;
const clientFns = new Set();
let match;
while ((match = exportedFnRegex.exec(clientSource)) !== null) {
  clientFns.add(match[1]);
}
console.log(`Found ${clientFns.size} exported functions in generated client`);

// Helper: PascalCase to camelCase
function toCamelCase(str) {
  return str[0].toLowerCase() + str.slice(1);
}

// Build the generated file
const lines = [];

// Header
lines.push("// GENERATED by manifest:generate-hooks — DO NOT EDIT.");
lines.push(
  "// TanStack Query hooks wrapping the generated client (manifest-client.generated.ts)."
);
lines.push("// Regenerate after IR changes.");
lines.push("");

// Imports — relative paths since hooks co-locate with the generated client
lines.push(
  'import { useQuery, useMutation, useQueryClient, type UseQueryOptions, type UseMutationOptions } from "@tanstack/react-query";'
);
lines.push('import * as client from "./manifest-client.generated";');
lines.push(
  'import type { PaginatedResponse } from "./manifest-client.generated";'
);
lines.push(
  'import { executeCommand, type CommandEnvelope } from "./manifest-client";'
);
lines.push("import type {");
for (const entity of entities) {
  lines.push(`  ${entity.name},`);
}
lines.push('} from "./manifest-types.generated";');
lines.push("");

// Query keys factory
lines.push("/**");
lines.push(" * Query key factory for all Manifest entities.");
lines.push(
  " * Usage: queryKeys.entityName.lists() / queryKeys.entityName.detail(id)"
);
lines.push(" */");
lines.push("export const queryKeys = {");
for (const entity of entities) {
  const camel = toCamelCase(entity.name);
  lines.push(`  ${camel}: {`);
  lines.push(`    all: ["${camel}"] as const,`);
  lines.push(`    lists: () => [...queryKeys.${camel}.all, "list"] as const,`);
  lines.push(
    `    detail: (id: string) => [...queryKeys.${camel}.all, "detail", id] as const,`
  );
  lines.push("  },");
}
lines.push("} as const;");
lines.push("");

// Entity list hooks
lines.push("// ============================================================");
lines.push("// List hooks (useEntityList)");
lines.push("// ============================================================");
lines.push("");

let listHookCount = 0;
const skippedList = [];

for (const entity of entities) {
  const name = entity.name;
  const camel = toCamelCase(name);
  const listFn = `list${name}s`;

  if (!clientFns.has(listFn)) {
    skippedList.push(name);
    continue;
  }
  listHookCount++;

  lines.push(`export function use${name}List(`);
  lines.push(
    `  options?: Omit<UseQueryOptions<PaginatedResponse<${name}>, Error>, "queryKey" | "queryFn">,`
  );
  lines.push(") {");
  lines.push("  return useQuery({");
  lines.push(`    queryKey: queryKeys.${camel}.lists(),`);
  lines.push(`    queryFn: () => client.${listFn}(),`);
  lines.push("    staleTime: 30_000,");
  lines.push("    ...options,");
  lines.push("  });");
  lines.push("}");
  lines.push("");
}

// Entity detail hooks
lines.push("// ============================================================");
lines.push("// Detail hooks (useEntityDetail)");
lines.push("// ============================================================");
lines.push("");

let detailHookCount = 0;
const skippedDetail = [];

for (const entity of entities) {
  const name = entity.name;
  const camel = toCamelCase(name);
  const getFn = `get${name}`;

  if (!clientFns.has(getFn)) {
    skippedDetail.push(name);
    continue;
  }
  detailHookCount++;

  lines.push(`export function use${name}Detail(`);
  lines.push("  id: string,");
  lines.push(
    `  options?: Omit<UseQueryOptions<${name} | undefined, Error>, "queryKey" | "queryFn">,`
  );
  lines.push(") {");
  lines.push("  return useQuery({");
  lines.push(`    queryKey: queryKeys.${camel}.detail(id),`);
  lines.push(`    queryFn: () => client.${getFn}(id),`);
  lines.push("    enabled: !!id,");
  lines.push("    staleTime: 30_000,");
  lines.push("    ...options,");
  lines.push("  });");
  lines.push("}");
  lines.push("");
}

// Command mutation hooks
lines.push("// ============================================================");
lines.push("// Command mutation hooks (useEntityCommandNameMutation)");
lines.push("// ============================================================");
lines.push("");

let mutationCount = 0;
for (const entity of entities) {
  const entityName = entity.name;
  const entityCamel = toCamelCase(entityName);
  const commands = entity.commands ?? [];

  for (const cmd of commands) {
    const cmdName = typeof cmd === "string" ? cmd : cmd.name;
    // Build mutation hook name
    const cmdPascal = cmdName[0].toUpperCase() + cmdName.slice(1);
    const hookName = `use${entityName}${cmdPascal}Mutation`;
    mutationCount++;

    // Command mutation hook — uses Record<string, unknown> for input since
    // IR commands are strings without param details in the entities array.
    lines.push(`export function ${hookName}(`);
    lines.push(
      `  options?: Omit<UseMutationOptions<CommandEnvelope<${entityName}>, Error, Record<string, unknown>>, "mutationFn">,`
    );
    lines.push(") {");
    lines.push("  const queryClient = useQueryClient();");
    lines.push("  return useMutation({");
    lines.push(
      `    mutationFn: (input) => executeCommand<${entityName}>("${entityName}", "${cmdName}", input),`
    );
    lines.push("    onSuccess: () => {");
    lines.push(
      `      queryClient.invalidateQueries({ queryKey: queryKeys.${entityCamel}.all });`
    );
    lines.push("    },");
    lines.push("    ...options,");
    lines.push("  });");
    lines.push("}");
    lines.push("");
  }
}

// Summary
lines.push("// ============================================================");
lines.push("// Summary");
lines.push("// ============================================================");
lines.push(`// Entities: ${entities.length}`);
lines.push(
  `// List hooks: ${listHookCount} (skipped ${skippedList.length}${skippedList.length > 0 ? ": " + skippedList.join(", ") : ""})`
);
lines.push(
  `// Detail hooks: ${detailHookCount} (skipped ${skippedDetail.length}${skippedDetail.length > 0 ? ": " + skippedDetail.join(", ") : ""})`
);
lines.push(`// Command mutation hooks: ${mutationCount}`);
lines.push(
  `// Total exports: ${listHookCount + detailHookCount + mutationCount}`
);
if (skippedList.length > 0) {
  lines.push(
    `// Skipped list hooks (no list fn in client): ${skippedList.join(", ")}`
  );
}

// Ensure output directory exists
mkdirSync(outDir, { recursive: true });

const outputPath = resolve(outDir, "manifest-hooks.generated.ts");
const content = lines.join("\n");
writeFileSync(outputPath, content, "utf-8");

console.log(`Generated hooks for ${entities.length} entities in ${outputPath}`);
console.log(`  List hooks: ${listHookCount} (skipped ${skippedList.length})`);
console.log(
  `  Detail hooks: ${detailHookCount} (skipped ${skippedDetail.length})`
);
console.log(`  Command mutation hooks: ${mutationCount}`);
console.log(
  `  Total exports: ${listHookCount + detailHookCount + mutationCount}`
);
console.log(`  Lines: ${content.split("\n").length}`);
if (skippedList.length > 0) {
  console.log(`  Skipped list: ${skippedList.join(", ")}`);
}
if (skippedDetail.length > 0) {
  console.log(`  Skipped detail: ${skippedDetail.join(", ")}`);
}
