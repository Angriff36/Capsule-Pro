#!/usr/bin/env node
import { execSync } from "node:child_process";
/**
 * generate-capsule-client — emit a typed frontend client that matches CAPSULE'S
 * conventions (manifest/capsule-conventions.json): domain read paths, real read
 * envelopes ({ <entityCamelPlural> }), canonical dispatcher writes ({ success,result,events }),
 * apiFetch, and the generated entity types. Replaces stock client paths that 404.
 *
 * DOMAIN-PARTITIONED (feature-1781435713420-506r2pr8i): the client is split into
 * domain-scoped chunks (events, kitchen, finance, staffing, crm, logistics + shared
 * core) so route-based layouts ship only their domain's callers instead of the
 * full ~1054-command monolith. Chunking derives from CLIENT_DOMAIN_MAP
 * (entity-domain-map.mjs) — the single source of truth shared with the hooks generator.
 *
 * Output layout (DO NOT EDIT):
 *   apps/app/app/lib/manifest-types.generated.ts            (types — type-only, no runtime payload)
 *   apps/app/app/lib/manifest-client.generated.ts           (shim → re-exports the barrel)
 *   apps/app/app/lib/manifest-client/core.generated.ts      (PaginatedResponse<T> shared primitive)
 *   apps/app/app/lib/manifest-client/<domain>.generated.ts  (one per non-core chunk)
 *   apps/app/app/lib/manifest-client/index.generated.ts     (barrel + loadClientDomain() dynamic loader)
 *
 * Run: pnpm manifest:client   (or: node manifest/scripts/generate-capsule-client.mjs)
 */
import {
  mkdirSync,
  mkdtempSync,
  readdirSync,
  readFileSync,
  statSync,
  writeFileSync,
} from "node:fs";
import { tmpdir } from "node:os";
import { dirname, join } from "node:path";
import {
  CLIENT_CHUNKS,
  ENTITY_DOMAIN_MAP,
  resolveClientChunk,
} from "./entity-domain-map.mjs";
import { paramTsType, toTsTypes } from "./scalar-type-map.mjs";

const conv = JSON.parse(
  readFileSync("manifest/capsule-conventions.json", "utf8")
);
const ir = JSON.parse(readFileSync("manifest/ir/kitchen.ir.json", "utf8"));
const camel = (s) => s.charAt(0).toLowerCase() + s.slice(1);
const cap = (s) => s.charAt(0).toUpperCase() + s.slice(1);
const plural = (s) =>
  /(s|x|z|ch|sh)$/.test(s)
    ? `${s}es`
    : /y$/.test(s)
      ? `${s.slice(0, -1)}ies`
      : `${s}s`;

// Domains that get their own runtime chunk (core holds only shared primitives).
const RUNTIME_CHUNKS = CLIENT_CHUNKS.filter((c) => c !== "core");

// 1) TYPES: generate via the official projection, copy into the app lib.
const tdir = mkdtempSync(join(tmpdir(), "mf-types-"));
execSync(
  `pnpm exec manifest generate manifest/ir/kitchen.ir.json --surface types --output "${tdir}"`,
  { stdio: "pipe" }
);
function find(dir, name) {
  for (const e of readdirSync(dir)) {
    const p = join(dir, e);
    if (statSync(p).isDirectory()) {
      const r = find(p, name);
      if (r) {
        return r;
      }
    } else if (e === name) {
      return p;
    }
  }
  return null;
}
const typesFile = find(tdir, "manifest-generated.ts");
const typesContent = readFileSync(typesFile, "utf8");
// The stock `types` surface emits raw Manifest scalar names (int/decimal/money/array/Date)
// as TS types, which don't compile. Map them to TS per Capsule's wire conventions
// (dates are ISO strings on the wire — manifest/capsule-conventions.json dateSerialization).
// Implementation now lives in scalar-type-map.mjs (D23 — single source of truth).
const tsTypes = toTsTypes(typesContent);
// The stock `types` surface references enum-typed columns (e.g. `status?: AdminTaskStatus`).
// Older manifest versions did not emit the enum type definitions themselves; current
// versions do. Emit ONLY the enums the stock surface leaves undefined (usually none
// now) so the generated types stay self-contained without duplicate identifiers.
const nativeEnumDefs = new Set(
  [...tsTypes.matchAll(/^export type (\w+) = /gm)].map((m) => m[1])
);
const enumDefs = (ir.enums ?? [])
  .filter((e) => !nativeEnumDefs.has(e.name))
  .map(
    (e) =>
      `export type ${e.name} = ${
        (e.values ?? []).map((v) => JSON.stringify(v.name)).join(" | ") || "string"
      };`
  )
  .join("\n");
writeFileSync(
  conv.client.typesOutput,
  "// GENERATED by manifest:client (manifest/scripts/generate-capsule-client.mjs) — DO NOT EDIT.\n" +
    (enumDefs ? enumDefs + "\n\n" : "") +
    tsTypes
);
const typeSet = new Set(
  [...tsTypes.matchAll(/export interface (\w+)/g)].map((m) => m[1])
);

// 2) CLIENT — partitioned into domain chunks.
const clientLibDir = dirname(conv.client.clientOutput);
const chunkDir = join(clientLibDir, "manifest-client");
mkdirSync(chunkDir, { recursive: true });

// Per-chunk state: out[] = function lines, interfaces[] = input type declarations,
// usedTypes = entity types referenced (for the targeted type import).
const chunkState = Object.fromEntries(
  CLIENT_CHUNKS.map((c) => [
    c,
    { out: [], interfaces: [], usedTypes: new Set(), entityCount: 0, cmdCount: 0 },
  ])
);
const overrides = conv.routePaths.commandOverrides || {};

// reads (domain-mapped entities)
for (const [entity, base] of Object.entries(ENTITY_DOMAIN_MAP)) {
  const ch = resolveClientChunk(entity);
  const st = chunkState[ch];
  st.entityCount++;
  const T = typeSet.has(entity) ? entity : "Record<string, unknown>";
  if (typeSet.has(entity)) {
    st.usedTypes.add(entity);
  }
  const listKey = plural(camel(entity));
  const detailKey = camel(entity);
  st.out.push(
    `export async function list${plural(entity)}(query?: Record<string, string | number>): Promise<PaginatedResponse<${T}>> {`
  );
  st.out.push(
    `  const qs = query ? "?" + new URLSearchParams(Object.entries(query).map(([k, v]) => [k, String(v)])).toString() : "";`
  );
  st.out.push(`  const res = await apiFetch(\`/api/${base}/list\${qs}\`);`);
  st.out.push(
    `  if (!res.ok) throw new Error(\`Failed to list ${entity} (\${res.status})\`);`
  );
  st.out.push("  const json = await res.json();");
  st.out.push(`  const data = (json.${listKey} ?? json.data ?? []) as ${T}[];`);
  st.out.push(
    "  const pagination = json.pagination ?? { page: 1, limit: data.length, total: data.length, totalPages: 1 };"
  );
  st.out.push("  return { data, pagination };");
  st.out.push("}");
  st.out.push(
    `export async function get${entity}(id: string): Promise<${T} | undefined> {`
  );
  st.out.push(
    `  const res = await apiFetch(\`/api/${base}/\${encodeURIComponent(id)}\`);`
  );
  st.out.push(
    `  if (!res.ok) throw new Error(\`Failed to get ${entity} (\${res.status})\`);`
  );
  st.out.push("  const json = await res.json();");
  // Hand-written detail routes return the entity at TOP LEVEL (no wrapper
  // key) — e.g. /api/kitchen/prep-lists/[id], /api/inventory/purchase-orders/[id].
  // Fall back to the whole payload when it looks like an entity (has an id).
  st.out.push(
    `  return (json.${detailKey} ?? json.data ?? (json.id ? json : undefined)) as ${T} | undefined;`
  );
  st.out.push("}");
}

// writes (all IR commands, via dispatcher unless overridden)
// Scalar→TS type mapping now lives in scalar-type-map.mjs (D23 — single source of truth).

// Generate per-command typed input interfaces
const seen = new Set();
for (const c of ir.commands) {
  const key = `${c.entity}.${c.name}`;
  if (seen.has(key)) {
    continue;
  }
  seen.add(key);
  const ch = resolveClientChunk(c.entity);
  const st = chunkState[ch];
  st.cmdCount++;
  const T = typeSet.has(c.entity) ? c.entity : "unknown";
  if (typeSet.has(c.entity)) {
    st.usedTypes.add(c.entity);
  }
  const fn = `${camel(c.entity)}${cap(c.name)}`;
  const ov = overrides[key];
  const pathArg = ov
    ? `, { path: \`${ov.replace(/\{(\w+)\}/g, '${String(input.$1 ?? "")}')}\` }`
    : "";

  // Build typed input interface from IR parameters.
  // All fields are optional (with `?`) because the Manifest runtime fills defaults
  // for omitted params and update commands accept partial input.
  let inputType = "Record<string, unknown>";
  if (c.parameters && c.parameters.length > 0) {
    const iName = `${c.entity}${cap(c.name)}Input`;
    const fields = c.parameters.map((p) => {
      const ts = paramTsType(p);
      return `  ${p.name}?: ${ts};`;
    });
    // Non-create commands accept `id` for instance resolution by the dispatcher
    // (body.id → deriveInstanceIdFromBody). It's not a Manifest command param
    // but part of the HTTP contract. Include it in the type so callers don't
    // need to cast.
    const hasId = c.parameters.some((p) => p.name === "id");
    if (!hasId && c.name !== "create") {
      fields.unshift("  id?: string;");
    }
    st.interfaces.push(`export interface ${iName} {\n${fields.join("\n")}\n}`);
    inputType = iName;
  } else if (c.name !== "create") {
    // Commands with no IR params but still need `id` for dispatcher
    const iName = `${c.entity}${cap(c.name)}Input`;
    st.interfaces.push(`export interface ${iName} {\n  id?: string;\n}`);
    inputType = iName;
  }

  st.out.push(
    `export async function ${fn}(input: ${inputType} = {}): Promise<${T} | undefined> {`
  );
  st.out.push(
    `  const r = await executeCommand<${T}>(${JSON.stringify(c.entity)}, ${JSON.stringify(c.name)}, input as Record<string, unknown>${pathArg});`
  );
  st.out.push("  return r.result;");
  st.out.push("}");
}

// ─── Emit core.generated.ts (shared primitives + core-domain callers) ─────
const coreState = chunkState.core;
const coreUsedTypes = [...coreState.usedTypes].sort();
const coreFile = [
  "// GENERATED by manifest:client — DO NOT EDIT.",
  "// Shared cross-domain primitives. Entity-specific callers live in the",
  "// sibling <domain>.generated.ts chunks. Regenerate after IR/domain-map changes.",
  'import { apiFetch } from "@/app/lib/api";',
  'import { executeCommand } from "@/app/lib/manifest-client";',
];
if (coreUsedTypes.length > 0) {
  coreFile.push(
    `import type { ${coreUsedTypes.join(", ")} } from "../manifest-types.generated";`
  );
}
coreFile.push(
  "",
  "// Re-exported here so the barrel can surface the shared primitive from one place.",
  "// (apiFetch / executeCommand are the only runtime cross-domain deps; every domain",
  "// chunk imports them directly from their canonical modules too.)",
  "export { apiFetch, executeCommand };",
  "",
  "/** Paginated list response matching Capsule API envelope. */",
  "export interface PaginatedResponse<T> {",
  "  data: T[];",
  "  pagination: { page: number; limit: number; total: number; totalPages: number };",
  "}",
  "",
);
if (coreState.interfaces.length > 0) {
  coreFile.push(
    "// Command input types (generated from IR parameters)",
    ...coreState.interfaces,
    ""
  );
}
coreFile.push(...coreState.out);
writeFileSync(join(chunkDir, "core.generated.ts"), coreFile.join("\n") + "\n");

// ─── Emit each domain chunk ────────────────────────────────────────────────
for (const ch of RUNTIME_CHUNKS) {
  const st = chunkState[ch];
  const used = [...st.usedTypes].sort();
  const lines = [
    "// GENERATED by manifest:client — DO NOT EDIT. Domain chunk: " + ch + ".",
    "// Matches Capsule conventions (manifest/capsule-conventions.json).",
    `import { apiFetch } from "@/app/lib/api";`,
    `import { executeCommand } from "@/app/lib/manifest-client";`,
    `import type { PaginatedResponse } from "./core.generated";`,
  ];
  if (used.length > 0) {
    lines.push(
      `import type { ${used.join(", ")} } from "../manifest-types.generated";`
    );
  }
  lines.push("");
  if (st.interfaces.length > 0) {
    lines.push("// Command input types (generated from IR parameters)");
    lines.push(...st.interfaces);
    lines.push("");
  }
  lines.push(...st.out);
  writeFileSync(join(chunkDir, `${ch}.generated.ts`), lines.join("\n") + "\n");
}

// ─── Emit index.generated.ts (barrel + dynamic domain loader) ──────────────
// The barrel statically re-exports every chunk so existing `@/app/lib/manifest-client.generated`
// imports keep working and remain tree-shakeable (unused re-exports are dropped).
// `loadClientDomain()` lets route-based layouts opt into an explicit async chunk via dynamic import().
const indexLines = [
  "// GENERATED by manifest:client — DO NOT EDIT. Barrel + dynamic domain loader.",
  "// Static re-exports are tree-shakeable; use loadClientDomain() for an async chunk.",
  'export * from "./core.generated";',
];
for (const ch of RUNTIME_CHUNKS) {
  indexLines.push(`export * from "./${ch}.generated";`);
}
indexLines.push("");
indexLines.push(
  `/** Client domain chunk names (core holds only shared primitives). */`
);
indexLines.push(
  `export type ClientDomain = ${RUNTIME_CHUNKS.map((c) => JSON.stringify(c)).join(" | ")};`
);
indexLines.push(`export const CLIENT_DOMAINS: readonly ClientDomain[] = [`);
for (const ch of RUNTIME_CHUNKS) {
  indexLines.push(`  ${JSON.stringify(ch)},`);
}
indexLines.push(`];`);
indexLines.push("");
// Type-only chunk imports (erased at compile → do NOT pull chunks eagerly).
for (const ch of RUNTIME_CHUNKS) {
  indexLines.push(`import type * as ${ch}Module from "./${ch}.generated";`);
}
indexLines.push(
  `interface ClientDomainModuleMap { ${RUNTIME_CHUNKS.map(
    (c) => `${c}: typeof ${c}Module`
  ).join("; ")}; }`
);
indexLines.push("");
indexLines.push(
  `const DOMAIN_LOADERS: Record<ClientDomain, () => Promise<unknown>> = {`
);
for (const ch of RUNTIME_CHUNKS) {
  indexLines.push(`  ${ch}: () => import("./${ch}.generated"),`);
}
indexLines.push(`};`);
indexLines.push("");
indexLines.push(
  `/** Dynamically load a domain's client chunk (creates a separate async JS bundle). */`
);
indexLines.push(
  `export async function loadClientDomain<D extends ClientDomain>(domain: D): Promise<ClientDomainModuleMap[D]> {`
);
indexLines.push(
  `  return DOMAIN_LOADERS[domain]() as Promise<ClientDomainModuleMap[D]>;`
);
indexLines.push(`}`);
writeFileSync(join(chunkDir, "index.generated.ts"), indexLines.join("\n") + "\n");

// ─── Emit backward-compat shim (keeps 100+ existing import paths working) ──
writeFileSync(
  conv.client.clientOutput,
  [
    "// GENERATED by manifest:client — DO NOT EDIT.",
    "// Backward-compat shim: re-exports the domain-partitioned barrel.",
    "// Direct consumers should migrate to `@/app/lib/manifest-client/<domain>.generated`",
    "// or `loadClientDomain()` (see ./manifest-client/index.generated) for payload splitting.",
    'export * from "./manifest-client/index.generated";',
    'export { loadClientDomain, CLIENT_DOMAINS } from "./manifest-client/index.generated";',
    'export type { ClientDomain } from "./manifest-client/index.generated";',
  ].join("\n") + "\n"
);

// ─── Summary ───────────────────────────────────────────────────────────────
console.log("WROTE", conv.client.typesOutput, `(${typeSet.size} types)`);
console.log("WROTE", join(chunkDir, "core.generated.ts"), "(shared primitives)");
for (const ch of RUNTIME_CHUNKS) {
  const st = chunkState[ch];
  console.log(
    `WROTE ${join(chunkDir, ch + ".generated.ts")} (${st.entityCount} entities, ${st.cmdCount} commands, ${st.interfaces.length} typed inputs)`
  );
}
console.log("WROTE", join(chunkDir, "index.generated.ts"), "(barrel + loader)");
console.log("WROTE", conv.client.clientOutput, "(backward-compat shim)");
console.log(
  `TOTAL ${Object.keys(ENTITY_DOMAIN_MAP).length} entities, ${seen.size} commands across ${RUNTIME_CHUNKS.length} domain chunks + core`
);
